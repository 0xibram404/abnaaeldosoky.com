document.addEventListener("DOMContentLoaded", () => {
	// --- MOBILE MENU LOGIC ---
	const mobileMenuButton = document.getElementById("mobile-menu-button")
	const mobileMenu = document.getElementById("mobile-menu")
	const toggleMobileMenu = () => {
		mobileMenu.classList.toggle("hidden")
		mobileMenuButton
			.querySelectorAll("svg")
			.forEach((icon) => icon.classList.toggle("hidden"))
	}
	mobileMenuButton.addEventListener("click", toggleMobileMenu)

	// --- LANGUAGE SWITCHER LOGIC ---
	const setLanguage = (lang) => {
		document.documentElement.lang = lang
		document.documentElement.dir = lang === "ar" ? "rtl" : "ltr"
		document.querySelectorAll("[data-key]").forEach((element) => {
			const key = element.dataset.key
			if (translations[lang] && translations[lang][key]) {
				element.innerHTML = translations[lang][key]
			}
		})
	}

	const toggleLanguage = () => {
		const newLang = document.documentElement.lang === "ar" ? "en" : "ar"
		setLanguage(newLang)
	}

	// Attach listeners to both desktop and mobile switchers
	document
		.getElementById("lang-switcher")
		.addEventListener("click", toggleLanguage)
	document
		.getElementById("lang-switcher-mobile")
		.addEventListener("click", toggleLanguage)

	// Close mobile menu when any link or button inside it is clicked
	document
		.querySelectorAll("#mobile-menu a, #mobile-menu button")
		.forEach((item) => {
			item.addEventListener("click", () => {
				if (!mobileMenu.classList.contains("hidden")) {
					toggleMobileMenu()
				}
			})
		})

	// --- INITIALIZATION ---
	setLanguage("ar")
})
